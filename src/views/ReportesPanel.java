    private void mostrarIngresosDiarios() {
        LocalDate hoy = LocalDate.now();
        double total = gestionFacturacion.calcularIngresosPorRango(hoy, hoy);

        StringBuilder sb = new StringBuilder();
        sb.append("═══════════════════════════════════════\n");
        sb.append("   REPORTE DE INGRESOS DEL DÍA\n");
        sb.append("═══════════════════════════════════════\n");
        sb.append("Fecha: ").append(hoy).append("\n");
        sb.append("───────────────────────────────────────\n");

        for (Factura f : gestionFacturacion.listarPorFecha(hoy)) {
            if (f.isPagado()) {
                sb.append(String.format("%-15s  S/. %,.2f  %s\n",
                    f.getNumeroFactura(),
                    f.getTotal(),
                    f.getPaciente().getNombreCompleto()));
            }
        }

        sb.append("───────────────────────────────────────\n");
        sb.append(String.format("TOTAL:  S/. %,.2f\n", total));
        sb.append("═══════════════════════════════════════\n");

        txtReporte.setText(sb.toString());
    }

