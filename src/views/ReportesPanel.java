    private void mostrarOcupacionConsultorios() {
        LocalDate hoy = LocalDate.now();
        StringBuilder sb = new StringBuilder();
        sb.append("═══════════════════════════════════════\n");
        sb.append("   OCUPACIÓN DE CONSULTORIOS\n");
        sb.append("═══════════════════════════════════════\n");
        sb.append("Fecha: ").append(hoy).append("\n");
        sb.append("───────────────────────────────────────\n");

        for (Consultorio consultorio : gestionConsultorios.listarTodos()) {
            sb.append("\n").append(consultorio.getCodigo())
                .append(" - ").append(consultorio.getNombre())
                .append(" (").append(consultorio.getEspecialidad()).append(")\n");
            sb.append("Estado: ").append(consultorio.getEstado()).append("\n");
            
            int citasOcupadas = 0;
            Map<LocalTime, String> horarioOcupado = new HashMap<>();
            
            for (Cita cita : gestionCitas.listarPorFecha(hoy)) {
                if (cita.getConsultorio() != null && 
                    cita.getConsultorio().getCodigo().equals(consultorio.getCodigo()) &&
                    cita.getEstado() != EstadoCita.CANCELADA) {
                    citasOcupadas++;
                    horarioOcupado.put(cita.getFechaHora().toLocalTime(), 
                                      cita.getPaciente().getNombreCompleto());
                }
            }
            
            sb.append("Citas programadas hoy: ").append(citasOcupadas).append("\n");
            
            if (citasOcupadas > 0) {
                sb.append("Horarios ocupados:\n");
                java.util.List<Map.Entry<LocalTime, String>> listaHorarios = new java.util.ArrayList<>(horarioOcupado.entrySet());
                
                for (int i = 0; i < listaHorarios.size() - 1; i++) {
                    for (int j = 0; j < listaHorarios.size() - i - 1; j++) {
                        if (listaHorarios.get(j).getKey().isAfter(listaHorarios.get(j + 1).getKey())) {
                            Map.Entry<LocalTime, String> temp = listaHorarios.get(j);
                            listaHorarios.set(j, listaHorarios.get(j + 1));
                            listaHorarios.set(j + 1, temp);
                        }
                    }
                }
                
                for (int i = 0; i < listaHorarios.size(); i++) {
                    Map.Entry<LocalTime, String> entry = listaHorarios.get(i);
                    sb.append("  ")
                        .append(entry.getKey())
                        .append(" - ")
                        .append(entry.getValue())
                        .append("\n");
                }
            }
        }

        sb.append("\n═══════════════════════════════════════\n");
        txtReporte.setText(sb.toString());
    }

    private void mostrarProduccionMedicos() {
        LocalDate hoy = LocalDate.now();
        LocalDate inicioMes = hoy.withDayOfMonth(1);
        LocalDate finMes = hoy.withDayOfMonth(hoy.lengthOfMonth());
        
        StringBuilder sb = new StringBuilder();
        sb.append("═══════════════════════════════════════\n");
        sb.append("   PRODUCCIÓN POR MÉDICO\n");
        sb.append("═══════════════════════════════════════\n");
        sb.append("Período: ").append(inicioMes).append(" al ").append(finMes).append("\n");
        sb.append("───────────────────────────────────────\n");

        for (Medico medico : gestionEmpleados.listarMedicos()) {
            sb.append("\n").append(medico.getNombreCompleto())
                .append(" - ").append(medico.getEspecialidad()).append("\n");
            
            int numConsultas = 0;
            for (Consulta consulta : gestionConsultas.listarPorFecha(inicioMes)) {
                if (consulta.getCita().getMedico().equals(medico)) {
                    LocalDate fechaConsulta = consulta.getFechaHoraInicio().toLocalDate();
                    if (!fechaConsulta.isBefore(inicioMes) && !fechaConsulta.isAfter(finMes)) {
                        numConsultas++;
                    }
                }
            }
            
            int numFacturas = 0;
            double totalFacturado = 0;
            for (Factura factura : gestionFacturacion.listarPorFecha(inicioMes)) {
                if (factura.getConsulta() != null && 
                    factura.getConsulta().getCita().getMedico().equals(medico)) {
                    LocalDate fechaFactura = factura.getFechaEmision().toLocalDate();
                    if (!fechaFactura.isBefore(inicioMes) && !fechaFactura.isAfter(finMes)) {
                        numFacturas++;
                        if (factura.isPagado()) {
                            totalFacturado += factura.getTotal();
                        }
                    }
                }
            }
            
            sb.append("  Consultas realizadas: ").append(numConsultas).append("\n");
            sb.append("  Facturas emitidas: ").append(numFacturas).append("\n");
            sb.append(String.format("  Total facturado: S/. %,.2f\n", totalFacturado));
        }

        sb.append("\n═══════════════════════════════════════\n");
        txtReporte.setText(sb.toString());
    }

